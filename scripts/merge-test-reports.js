function renderSuite(suite) {
  return `<section class="suite">
    <h2>${escapeHtml(suite.label)}</h2>
    <p>Exit code: <strong>${suite.exitCode}</strong> | Duration: <strong>${formatDuration(suite.durationMs)}</strong></p>
    <table>
      <tr><th>Total</th><th>Passed</th><th>Failed</th><th>Skipped</th></tr>
      <tr><td>${suite.total}</td><td>${suite.passed}</td><td>${suite.failed}</td><td>${suite.skipped}</td></tr>
    </table>
    <div class="case-list">
      ${suite.tests.map(renderCase).join("") || "<p>No test details were available.</p>"}
    </div>
  </section>`;
}

function renderCase(test) {
  const statusClass =
    test.status === "passed"
      ? "passed"
      : test.status === "failed"
        ? "failed"
        : "skipped";

  return `<article class="case">
    <div class="badge ${statusClass}">${escapeHtml(test.status.toUpperCase())}</div>
    <h3>${escapeHtml(test.name)}</h3>
    <p><strong>File:</strong> ${escapeHtml(test.file || "N/A")}</p>
    <p><strong>Duration:</strong> ${formatDuration(test.durationMs || 0)}</p>
    ${
      test.error
        ? `<pre>${escapeHtml(test.error)}</pre>`
        : "<p>No error message.</p>"
    }
  </article>`;
}

function parseJestLikeResults(payload, label) {
  const rootResults = payload.testResults ?? payload.files ?? [];
  const tests = [];

  for (const fileResult of rootResults) {
    const assertionResults =
      fileResult.assertionResults ??
      fileResult.tests ??
      fileResult.tasks ??
      [];

    for (const assertion of assertionResults) {
      const status = normalizeStatus(assertion.status ?? assertion.result?.state);
      const error = Array.isArray(assertion.failureMessages)
        ? assertion.failureMessages.join("\n\n")
        : assertion.errors?.join?.("\n\n") ?? "";

      tests.push({
        status,
        name:
          assertion.fullName ??
          assertion.name ??
          assertion.title ??
          "Unnamed test",
        file: fileResult.name ?? fileResult.file ?? fileResult.filepath ?? "Unknown file",
        durationMs:
          assertion.duration ?? assertion.durationMs ?? assertion.result?.duration ?? 0,
        error,
      });
    }
  }

  return summarizeSuite(label, tests, payload);
}

function parseJUnitResults(xml, label) {
  if (!xml) {
    return summarizeSuite(label, [], {});
  }

  const tests = [];
  const testcasePattern = /<testcase\b([^>]*)>([\s\S]*?)<\/testcase>|<testcase\b([^>]*)\/>/g;
  let match;

  while ((match = testcasePattern.exec(xml)) !== null) {
    const attributeChunk = match[1] || match[3] || "";
    const body = match[2] || "";
    const name = decodeXml(readAttribute(attributeChunk, "name") || "Unnamed test");
    const file = decodeXml(
      readAttribute(attributeChunk, "classname") || readAttribute(attributeChunk, "file") || "Unknown file",
    );
    const durationMs = Number(readAttribute(attributeChunk, "time") || 0) * 1000;

    let status = "passed";
    let error = "";

    if (body.includes("<failure")) {
      status = "failed";
      error = decodeXml(extractTagText(body, "failure"));
    } else if (body.includes("<error")) {
      status = "failed";
      error = decodeXml(extractTagText(body, "error"));
    } else if (body.includes("<skipped")) {
      status = "skipped";
      error = decodeXml(extractTagText(body, "skipped"));
    }

    tests.push({ status, name, file, durationMs, error });
  }

  return summarizeSuite(label, tests, {});
}

function summarizeSuite(label, tests, payload) {
  return {
    key: label.toLowerCase(),
    label,
    total: payload.numTotalTests ?? tests.length,
    passed:
      payload.numPassedTests ??
      tests.filter((test) => test.status === "passed").length,
    failed:
      payload.numFailedTests ??
      tests.filter((test) => test.status === "failed").length,
    skipped:
      payload.numPendingTests ??
      payload.numTodoTests ??
      tests.filter((test) => test.status === "skipped").length,
    durationMs: payload.startTime && payload.success !== undefined ? 0 : 0,
    tests,
  };
}

function normalizeStatus(status) {
  if (status === "passed" || status === "fail" || status === "failed") {
    return status === "fail" ? "failed" : status;
  }
  if (status === "pending" || status === "skipped" || status === "todo") {
    return "skipped";
  }
  return "passed";
}

function readJson(filePath, fallback) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch {
    return fallback;
  }
}

function readFile(filePath) {
  try {
    return fs.readFileSync(filePath, "utf8");
  } catch {
    return "";
  }
}

function readAttribute(attributes, name) {
  const match = attributes.match(new RegExp(`${name}="([^"]*)"`, "i"));
  return match ? match[1] : "";
}

function extractTagText(body, tagName) {
  const match = body.match(new RegExp(`<${tagName}[^>]*>([\\s\\S]*?)<\\/${tagName}>`, "i"));
  return match ? match[1] : "";
}

function decodeXml(value) {
  return value
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, "\"")
    .replace(/&#39;/g, "'")
    .replace(/&amp;/g, "&");
}

function escapeHtml(value) {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function formatDuration(durationMs) {
  if (!durationMs) {
    return "0 ms";
  }

  if (durationMs < 1000) {
    return `${Math.round(durationMs)} ms`;
  }

  return `${(durationMs / 1000).toFixed(2)} s`;
}
